package Castings;

public class Castings {

	public static void main(String[] args) {
		int numero1 = 15;
		int numero2 = 4;
		int soma = numero1 + numero2;
		double divisao = (double)numero1 / (double)numero2;
		System.out.println(soma);
		System.out.println(divisao);
		
		char a = 'a';
		char b = 'b';
		
		int soma1 = a+b;
		int inteiro = 76;
		char caracter = (char)inteiro;
		System.out.print("A soma é:"+soma1+"\n");
		System.out.print("O caracter é:"+ caracter);
		
	}

}
